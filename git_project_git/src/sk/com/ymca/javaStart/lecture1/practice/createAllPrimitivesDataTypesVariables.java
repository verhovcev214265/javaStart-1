package sk.com.ymca.javaStart.lecture1.practice;

public class createAllPrimitivesDataTypesVariables {
    public static void main(String[] args) {
        byte firstVariable = 127;
        short secondVariable = 3200;
        int thirdVariable = 37896;
        long fourthVariable = 1000000;
        char fifthVariable = 'e';
        float sixthVariable = 2.5F;
        double seventhVariable = 40.65;
        boolean eighthVariable = false;

        System.out.println(firstVariable + "\n" + secondVariable + "\n" + thirdVariable + "\n" + fourthVariable + "\n"
                + fifthVariable + "\n" + sixthVariable + "\n" + seventhVariable + "\n" + eighthVariable);
    }
}
